export type TaskFocus = {
  title: string;
  check: string;
  keyObservation: string;
  mistakeKind: string;
  selfCheck: string;
  diagnosticPrompt: string;
  visualPrompt?: string;
};

type TaskFocusInput = {
  blueprint: string;
  skill?: string;
};

type TaskFocusSeed = {
  title: string;
  check: string;
  keyObservation?: string;
  mistakeKind?: string;
  selfCheck?: string;
  diagnosticPrompt?: string;
  visualPrompt?: string;
};

function buildFocus({
  title,
  check,
  keyObservation,
  mistakeKind,
  selfCheck,
  diagnosticPrompt,
  visualPrompt,
}: TaskFocusSeed): TaskFocus {
  return {
    title,
    check,
    keyObservation:
      keyObservation ??
      `Сначала распознай тип задачи: ${title.toLowerCase()}.`,
    mistakeKind:
      mistakeKind ??
      "Обычно здесь сбивает быстрый расчёт без проверки, какая физическая связь нужна.",
    selfCheck:
      selfCheck ??
      "Сможешь объяснить первый шаг решения до подстановки чисел?",
    diagnosticPrompt:
      diagnosticPrompt ??
      "Какой признак в условии показывает, что выбранный ход не подходит?",
    visualPrompt,
  };
}

