def now_iso():
    return datetime.now(timezone.utc).isoformat()


async def seed_learning_methodology(db):
    for path in LEARNING_PATHS:
        await db.learning_paths.update_one({"slug": path["slug"]}, {"$set": {**path, "updated_at": now_iso()}}, upsert=True)

    for title, update in COURSE_UPDATES.items():
        await db.courses.update_one({"title": title}, {"$set": {**update, "updated_at": now_iso()}})

    for title, update in CHAPTER_ENRICHMENT.items():
        await db.chapters.update_one({"title": title}, {"$set": {**update, "updated_at": now_iso()}})

    for course_title, chapters in EXAM_CHAPTERS.items():
        course = await db.courses.find_one({"title": course_title})
        if not course:
            continue
        course_id = str(course["_id"])
        for chapter in chapters:
            await db.chapters.update_one(
                {"course_id": course_id, "title": chapter["title"]},
                {"$set": {**chapter, "course_id": course_id, "updated_at": now_iso()}},
                upsert=True,
            )
        count = await db.chapters.count_documents({"course_id": course_id})
        await db.courses.update_one({"_id": course["_id"]}, {"$set": {"chapters_count": count, "updated_at": now_iso()}})

    await db.exam_variants.update_one(
        {"slug": EXAM_VARIANT_V2["slug"]},
        {"$set": {**EXAM_VARIANT_V2, "updated_at": now_iso()}},
        upsert=True,
    )
